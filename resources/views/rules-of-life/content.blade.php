<p>&nbsp;</p>

<ul>
	<li>Гость принимает к сведению и не возражает против факта использования на территории и в помещении отеля (за исключением гостевых номеров) систем видеонаблюдения.</li>
	<li>Курение на территории отеля и в номерах строго запрещено согласно ФЗ № 15 от 23 февраля 2013 &laquo;Об охране здоровья граждан от воздействия окружающего табачного дыма и последствий потреблении табака&raquo;.</li>
	<li>В период проживания, необходимо сохранять тишину с 23.00 ч. до 8.00 ч. утра в номерах и на территории, запрещается включать на большую громкость аудиосистемы, телевизоры.</li>
	<li>По просьбе проживающих, с согласия администрации, допускается нахождение посторонних лиц в номере с 08:00 до 23:00 часов; для этого посетителю необходимо предоставить администратору на стойке регистрации удостоверение личности и получить гостевую карту.</li>
	<li>В случае задержки посетителя в номере гостя после 23:00 или провода в гостиницу зарегистрированным гостем постороннего лица в ночное время (с 23:00 до 08:00 часов) данные лица должны быть оформлены на подселение в номер гостя. За такое подселение взимается оплата согласно утвержденного прейскуранта.</li>
	<li>Парковка автотранспорта осуществляется на территории Отеля в специально установленном месте, за дополнительную оплату в размере трехсот рублей. Запрещается оставлять транспортные средства на заездах и выездах. Парковка машин на неустановленной территории отеля запрещена.</li>
	<li>Отель не несет ответственности за работу городских служб (аварийное отключение электрической и тепловой энергии, водоснабжения).</li>
	<li>При отсутствии гостя по месту проживания по истечении 3&nbsp; часов с момента наступления его расчетного часа, администрация гостиницы вправе создать комиссию и сделать опись имущества, находящегося в номере. Материальные ценности в виде денежных средств, драгоценных металлов, ценных документов, администрация берет под свою ответственность. Прочее имущество находится в камере хранения.</li>
	<li>Администрация не несет ответственности за утрату ценных вещей гостя, находящихся в номере, при нарушении им порядка проживания в гостинице. В случае обнаружения забытых вещей администрация принимает меры к возврату их владельцам .</li>
	<li>Отель оставляет&nbsp; за собой&nbsp; право отказать Гостю в предоставлении услуг проживания и&nbsp; посещение зон отдыха в случаях нарушения настоящих Правил, несвоевременной оплаты за проживание и предоставленные дополнительные услуги, проявления со стороны Гостя в отношении персонала и других отдыхающих агрессии или действий, угрожающих безопасности здоровья или имущества других лиц.</li>
	<li>Любые разногласия, не нашедшие своего разрешения в тексте данных Правил, будут разрешаться путем переговоров. При невозможности урегулирования в процессе переговоров спорных вопросов, споры разрешаются в порядке, установленном действующим законодательством РФ.</li>
	<li>В случае несоблюдения Гостем вышеуказанных требований настоящих Правил, администрация отеля вправе досрочно выселить его.</li>
	<li>Книга отзывов и предложений находится у администратора отеля и выдается по первому требованию Гостя.</li>
</ul>

