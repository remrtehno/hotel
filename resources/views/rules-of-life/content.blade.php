<h1>Правила проживания и посещения</h1>

<h2>в отеле &laquo;Royal Plaza&raquo;</h2>

<h4>&nbsp;</h4>

<ul>
	<li>Отель предназначен для временного проживания гостей на срок, согласованный с Администрацией.</li>
	<li>Отель&nbsp;&laquo;Royal Plaza&raquo;&nbsp;открыт для Вас круглосуточно.</li>
	<li>Заезд Гостей производится с 14:00 по местному времени дня заезда, расчетный час &ndash; 12:00 по местному времени в день отъезда.</li>
	<li>Ранний заезд возможен при наличии свободных мест, по предварительному заказу.</li>
	<li>Доплата за ранний заезд с 00:00 до 06:00 - 100% от стоимости номера, с 06:00 до 14:00 &ndash; 50% от стоимости забронированного номера.</li>
	<li>Номер предоставляется гостю при полной оплате стоимости номера, по предъявлению паспорта, а для иностранных граждан - загранпаспорт, виза и миграционная карта, а также внесении депозита в размере 3&nbsp;000,00 рублей. При оформлении проживания в отеле гостю необходимо поставит свою подпись на регистрационной карте, после ознакомления с правилами проживания.</li>
	<li>Оплата проживания за наличный расчет производится в момент заселения в отель.</li>
	<li>В случае, если оплата производится по безналичному расчету и на момент прибытия Гостя оплата не поступила на расчетный счет, то Гость оплачивает проживание наличными. По поступлению безналичных средств, наличная оплата возвращается.</li>
	<li>При проживании, первые сутки менее 24 часов, оплата за проживание взимается за полные сутки, независимо от времени заезда и выезда.</li>
	<li>При досрочном выезде из отеля удерживается оплата за номер за одни последующие сутки. Дата выезда дополнительно подтверждается гостем при заезде.</li>
	<li>Гость имеет право на бесплатное размещение в одном номере&nbsp; детей в возрасте до шести лет,&nbsp; без предоставления дополнительного спального места (для детей в возрасте до трех лет детская кровать предоставляется бесплатно).</li>
	<li>Гость имеет право на размещение в одном номере ребенка, в возрасте от 6 до 12 лет, за дополнительную оплату в размере 1500 рублей&nbsp; в сутки, без предоставления дополнительного спального места. За дополнительное спальное место взимается оплата в размере 2000 рублей в сутки.</li>
	<li>Дети в возрасте двенадцати лет и старше размещаются в Отеле согласно установленным тарифам на проживание Гостей.</li>
	<li>Продление проживания возможно при наличии свободных мест. При позднем выезде Гостя с 12.00 до 14.00 часов по местному времени взимается оплата в размере 500р., с 14.00 до 18.00 часов взимается оплата за половину суток проживания, с 18.00 до 12.00 следующего дня по местному времени взимается оплата в размере полных суток.</li>
	<li>Уборка в номерах производится ежедневно.</li>
	<li>Дополнительная смена постельного белья и махры оплачивается отдельно.</li>
	<li>К Вашим услугам без дополнительной оплаты: беспроводной доступ в Интернет, справочная информация, вызов такси, побудка к определенному времени, вызов скорой помощи, пользование медицинский аптечкой, доставка в номер корреспонденции при ее получении, чистка обуви</li>
</ul>

