<p><strong>С целью обеспечения порядка и безопасности гостей, в отеле запрещается:</strong></p>

<ul>
	<li>&mdash; оставлять посторонних лиц в номере в свое отсутствие;</li>
	<li>&mdash; передавать посторонним лицам ключ и/или карту гостя;</li>
	<li>&mdash; хранить в номерах громоздкие вещи, легковоспламеняющиеся, взрывчатые, наркотические вещества и материалы;</li>
	<li>&mdash; пользоваться нагревательными приборами, если это не предусмотрено в номере;</li>
	<li>&mdash; нарушать покой гостей.</li>
</ul>

<p>&nbsp;</p>

<p><strong>Гость должен:</strong></p>

<ul>
	<li>&mdash; Соблюдать правила проживания и посещения.</li>
	<li>&mdash; Своевременно и в полном объеме оплачивать все предоставленные Отелем дополнительные услуги.</li>
	<li>&mdash; Возместить ущерб в случае утраты, повреждения или порчи имущества Отеля.</li>
	<li>&mdash; Соблюдать дресс-код.</li>
	<li>&mdash; Нести ответственность за приглашенных гостей.</li>
</ul>
